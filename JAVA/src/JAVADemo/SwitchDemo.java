package JAVADemo;

public class SwitchDemo {

	public static void main(String[] args) {
		int x = 100;
		switch(x){
		case 1:
			System.out.println("abcd");
		case 2:
			System.out.println("123");
		case 100:
			System.out.println("Reshma");
			break;
			
			default :
				System.out.println("Good");
			
		}

	}

}
